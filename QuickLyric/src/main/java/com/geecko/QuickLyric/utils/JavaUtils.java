package com.geecko.QuickLyric.utils;


public class JavaUtils {

    public static void postDelayed(final Runnable r, final long millis) {
        new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    Thread.sleep(millis);
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
                r.run();
            }
        }).start();
    }

}
